#ifndef _BOXCOLLIDERCOMPONENT_H_
#define _BOXCOLLIDERCOMPONENT_H_

////////////////////////////////////////////////////////////
// Includes
////////////////////////////////////////////////////////////
///Frox
#include "Component.h"

///SFML 
#include  "SFML/Graphics/Rect.hpp"

class BoxColliderComponent : public Component
{
public:
	////////////////////////////////////////////////////////////
	/// Overloaded constructor
	////////////////////////////////////////////////////////////
	BoxColliderComponent() : Component("BoxColliderComponent"),
		m_collision(false){}

	////////////////////////////////////////////////////////////
	/// Destructor (for overriding virtual function in abstract 
	/// base Component)
	////////////////////////////////////////////////////////////
	~BoxColliderComponent() {}

	////////////////////////////////////////////////////////////
	/// Getters/Setters
	////////////////////////////////////////////////////////////
	sf::FloatRect getBox() { return m_box; }
	void setBox(sf::FloatRect box) { m_box = box; }

	bool isColliding() { return m_collision; }

private:
	////////////////////////////////////////////////////////////
	/// Member Variables
	////////////////////////////////////////////////////////////
	sf::FloatRect m_box;
	bool m_collision;

};
#endif;